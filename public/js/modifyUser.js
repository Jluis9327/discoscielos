
function deshabilitar(ine){
    document.getElementById("name"+ine).disabled=false;
    document.getElementById("ape"+ine).disabled=false;
    document.getElementById("dni"+ine).disabled=false;
    document.getElementById("phone"+ine).disabled=false;
    document.getElementById("password"+ine).disabled=false;
    document.getElementById("email"+ine).disabled=false;
    document.getElementById("estado"+ine).disabled=false;
}
